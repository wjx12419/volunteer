export function getImgSrc(richtext,num){
	let imgList = [];
	richtext.replace(/img[^>]*src=['"]([^'"]+)[^>]/g,(match,capture)=>{
		imgList.push(capture);
	});
	imgList = imgList.slice(0,num);
	return imgList;
}
