function getIp(){
	return new Promise((resolve,reject)=>{
		uni.request({
			url:"https://restapi.amap.com/v3/ip?key=f3346ff1eb17564d2924e5312768b3ff",
			success: (res) => {
				
				let str="";
				typeof(res.data.province=="string")?str = res.data.province:str="获取地址异常！";
				resolve(str);
				let obj = {
					province:str,
					time:Date.now()
				}
				uni.setStorageSync("historyProvince",obj);
			},
			fail: (err) => {
				reject(err);
			}
		})
	})
}
